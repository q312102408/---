#define gdt_大小 (2 * h_一页大小)

#define h_内核页表_起点 h_gdt_实地址 + gdt_大小

#define h_内核页表个数 (20 * 1024 * 1024 / 4096 / 1024)

#define h_内核全部页表个数 (1000 * 1024 * 1024 / 4096 / 1024)

// 20M以上的区域,为没用过的地方
//#define h_物理地址空白区 (20 * 1024 * 1024)
