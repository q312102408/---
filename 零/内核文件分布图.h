#define h_内核堆栈起点 (h_虚拟地址起点 + 500 * 1024 * 1024)
#define h_内核栈顶点 (h_虚拟地址起点 + 900 * 1024 * 1024 - 1)

#define h_内核目录_地址 (h_内核目录_实地址 + h_虚拟地址起点)
